#-------------------------------------------------------------------------------
# MessageProxy
#
# Proxy class for gathering new message data
#
#-------------------------------------------------------------------------------
class FacilityRollupAssetTypeProxy < Proxy

  #-----------------------------------------------------------------------------
  # Attributes
  #-----------------------------------------------------------------------------
  attr_accessor :quantity
  attr_accessor :asset_type_id
  attr_accessor :asset_subtype_id
  attr_accessor :column

  #-----------------------------------------------------------------------------
  # Validations
  #-----------------------------------------------------------------------------

  validates_presence_of :quantity

  #-----------------------------------------------------------------------------
  # Constants
  #-----------------------------------------------------------------------------

  # List of allowable form param hash keys
  FORM_PARAMS = [
      :quantity,
      :asset_type_id,
      :asset_subtype_id,
      :column
  ]

  #-----------------------------------------------------------------------------
  #
  # Class Methods
  #
  #-----------------------------------------------------------------------------

  def self.allowable_params
    FORM_PARAMS
  end

  #-----------------------------------------------------------------------------
  #
  # Instance Methods
  #
  #-----------------------------------------------------------------------------

  #-----------------------------------------------------------------------------
  #
  # Protected Methods
  #
  #-----------------------------------------------------------------------------
  protected

  def initialize(attrs = {})
    super
    attrs.each do |k, v|
      self.send "#{k}=", v
    end
  end

end
